        // TODO add your handling code here:
        //modelo.setRowCount(0);
        año = Integer.parseInt(String.valueOf(añoSp.getValue()));
        mes = Integer.parseInt(String.valueOf(mesSp.getValue()));
        dia = Integer.parseInt(String.valueOf(diaSp.getValue()));
        if(mes < 10 && dia < 10){
            fecha = año + "-0" + mes + "-0" + dia;
        } else if (mes < 10){
            fecha = año + "-0" + mes + "-" + dia;
        } else if (dia < 10){
            fecha = año + "-" + mes + "-0" + dia;
        } else{
            fecha = año + "-" + mes + "-" + dia;
        }
        String sql = "SELECT numVent, met_pag, monto_pagado, cambio, numEmp FROM ventas where fec like '%" + fecha + "%'";
        imprimirTabla(sql);
        //System.out.println(fecha);
