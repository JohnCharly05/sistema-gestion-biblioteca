    public void imprimirTabla(String consulta){
        try {
            statement = connection.createStatement();
            //String sql = "SELECT codLibro, nom_Lib FROM Libros";
            resultSet = statement.executeQuery(consulta);
            Object[] venta = new Object[5];
            modelo = (DefaultTableModel) tablaCorte.getModel();
            while (resultSet.next()) {
                venta[0] = resultSet.getInt("numVent");
                venta[1] = resultSet.getString("met_pag");
                venta[2] = resultSet.getString("monto_pagado");
                venta[3] = resultSet.getString("cambio");
                venta[4] = resultSet.getString("numEmp");
                modelo.addRow(venta);
            }
            tablaCorte.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(MenuLibros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
